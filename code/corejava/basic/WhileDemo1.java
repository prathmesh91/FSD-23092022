public class WhileDemo1
{
	public static void main(String arr[])
	{
		int i = 1;
		while(i<=10)
		{
			System.out.println(i);
			i++;
		}

		System.out.println("=================");

		int j = 10;
		while(j >= 1)
		{
			System.out.println(j);
			j--;
		}

		System.out.println("=================");

		char ch = 'A';
		while(ch != 'Z') {
			System.out.println(ch);
			ch++;
		}


	}
}